export const lookupCities = async (req: Request, res: Response) => {
  const { cities } = req.body as { cities: string[] };
  if (!Array.isArray(cities) || cities.length === 0)
    return res.json({ mapping: {} });

  const result: Record<string, string> = {};
  const clean = (s: string) => (s || '').toUpperCase().trim();

  // 1) Consultar todas las ciudades conocidas en DB de una sola vez
  const upperCities = cities.map(clean);
  const { rows: dbRows } = await pool.query(
    `SELECT c.nombre, d.nombre AS departamento
     FROM cfg_ciudades c
     JOIN cfg_departamentos d ON d.id = c.id_departamento
     WHERE UPPER(TRIM(c.nombre)) = ANY($1)`,
    [upperCities]
  );
  for (const row of dbRows) result[clean(row.nombre)] = row.departamento;

  // 2) Para las desconocidas, buscar en BASE_MAP y luego en Nominatim
  const unknown = upperCities.filter(c => !result[c]);
  for (const city of unknown) {
    // Primero checar mapa base
    let dept = BASE_MAP[city] || null;
    let source = 'base_map';

    // Si no está, buscar en Nominatim
    if (!dept) {
      const raw = await lookupNominatim(city);
      if (raw) { dept = normalizeDept(raw); source = 'nominatim'; }
    }

    if (dept) {
      result[city] = dept;
      // Guardar en DB para aprender
      try {
        // Obtener o crear departamento
        let { rows: deptRows } = await pool.query(
          `SELECT id FROM cfg_departamentos WHERE UPPER(TRIM(nombre)) = $1`, [dept]
        );
        let deptId: number;
        if (deptRows.length === 0) {
          const ins = await pool.query(
            `INSERT INTO cfg_departamentos (nombre, estado, usuario_control) VALUES ($1, 'EST-01', 'System') RETURNING id`,
            [dept]
          );
          deptId = ins.rows[0].id;
        } else {
          deptId = deptRows[0].id;
        }
        // Insertar ciudad
        await pool.query(
          `INSERT INTO cfg_ciudades (nombre, id_departamento, estado, usuario_control)
           VALUES ($1, $2, 'EST-01', $3)
           ON CONFLICT DO NOTHING`,
          [city, deptId, source === 'nominatim' ? 'Nominatim' : 'System']
        );
      } catch (e) { /* continuar aunque falle el insert */ }
    }
  }

  res.json({ mapping: result });
};
