// Rate limit Nominatim: 1 req/seg máximo
let lastNominatimCall = 0;
const waitRateLimit = async () => {
  const elapsed = Date.now() - lastNominatimCall;
  if (elapsed < 1200) await new Promise(r => setTimeout(r, 1200 - elapsed));
  lastNominatimCall = Date.now();
};

const httpsGet = (url: string): Promise<any[]> =>
  new Promise((resolve, reject) => {
    const parsed = new URL(url);
    https.get(
      { hostname: parsed.hostname, path: parsed.pathname + parsed.search, headers: { 'Accept-Language': 'es', 'User-Agent': 'OrbitM7-Logistics/1.9 (logistics@millasiete.com)' } },
      res => {
        let raw = '';
        res.on('data', c => (raw += c));
        res.on('end', () => { try { resolve(JSON.parse(raw)); } catch { resolve([]); } });
      }
    ).on('error', () => resolve([]));
  });

