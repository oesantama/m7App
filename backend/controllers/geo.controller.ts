// Busca departamento en Nominatim para una ciudad colombiana
const lookupNominatim = async (city: string): Promise<string | null> => {
  try {
    await waitRateLimit();
    const q = encodeURIComponent(`${city}, Colombia`);
    const url = `https://nominatim.openstreetmap.org/search?q=${q}&format=json&addressdetails=1&limit=1&countrycodes=co`;
    const results = await httpsGet(url);
    if (results.length > 0) {
      const addr = results[0].address || {};
      const state = addr.state || addr.region || '';
      if (state) return state.toUpperCase().replace(/\s+/g, ' ').trim();
    }
  } catch { }
  return null;
};

